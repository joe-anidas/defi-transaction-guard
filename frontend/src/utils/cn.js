import { clsx } from 'clsx'

/**
 * Utility function to merge class names with clsx
 * Provides better TypeScript support and cleaner syntax
 */
export function cn(...inputs) {
  return clsx(inputs)
}

/**
 * Conditional class utility for complex conditions
 */
export function conditionalClasses(classes) {
  return clsx(classes)
}

